package com.example;
import java.util.*;
public class EvenorOdd {
	
	public static void main(String args[])
	{
		Scanner sc= new Scanner(System.in);
		System.out.println("Enter the number you want");
		int number= sc.nextInt();
		if(number%2==0)
		{
			System.out.println("EVEN");
			
		}
		else
		{
			System.out.println("ODD");
		}
		
	
	}
	

}


